    {
        Time.timeScale = 1.0f;

        AsyncOperation gameLoad = SceneManager.LoadSceneAsync("GameLevel 1");

        // Wait until the asynchronous scene fully loads
        while (!gameLoad.isDone)
        {
            yield return null;
        }
    }

